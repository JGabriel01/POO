class calculadora { // pertence a classe
    static somar(a,b) {
        return a+b
    }
    static subtrair(a,b) {
        return a-b
    }
    static multiplicar(a,b) {
        return a*b
    }
}

console.log(calculadora.somar(1,2))
console.log(calculadora.subtrair(1,2))
console.log(calculadora.multiplicar(3,2))